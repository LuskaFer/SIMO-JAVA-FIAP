
public class CargoDesejado extends Usuario{

	private String cargoDesejado;
	private double faixaSalarial;
	private String cidade;

	public String getCargoDesejado() {
		return cargoDesejado;
	}

	public void setCargoDesejado(String cargoDesejado) {
		this.cargoDesejado = cargoDesejado;
	}

	public double getFaixaSalarial() {
		return faixaSalarial;
	}

	public void setFaixaSalarial(double faixaSalarial) {
		this.faixaSalarial = faixaSalarial;
	}

	public String getCidade() {
		return cidade;
	}

	public void setCidade(String cidade) {
		this.cidade = cidade;
	}

}
