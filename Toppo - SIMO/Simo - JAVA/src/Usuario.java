
public class Usuario extends Endereco {

	private String nome;
	private String genero;
	private String telefone;
	private String dataDeNascimento;
	private String nacionalidade;
	private String estadoCivil;
	private String rg;
	private String cpf;
	private String HistoricoEscolar;
	private String HistoricoProfissional;
	private String CargoDesejado;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getGenero() {
		return genero;
	}

	public void setGenero(String genero) {
		this.genero = genero;
	}

	public String getTelefone() {
		return telefone;
	}

	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}

	public String getDataDeNascimento() {
		return dataDeNascimento;
	}

	public void setDataDeNascimento(String dataDeNascimento) {
		this.dataDeNascimento = dataDeNascimento;
	}

	public String getNacionalidade() {
		return nacionalidade;
	}

	public void setNacionalidade(String nacionalidade) {
		this.nacionalidade = nacionalidade;
	}

	public String getEstadoCivil() {
		return estadoCivil;
	}

	public void setEstadoCivil(String estadoCivil) {
		this.estadoCivil = estadoCivil;
	}

	public String getRg() {
		return rg;
	}

	public void setRg(String rg) {
		this.rg = rg;
	}

	public String getCpf() {
		return cpf;
	}

	public void setCpf(String cpf) {
		this.cpf = cpf;
	}

	public String getHistoricoEscolar() {
		return HistoricoEscolar;
	}

	public void setHistoricoEscolar(String historicoEscolar) {
		HistoricoEscolar = historicoEscolar;
	}

	public String getHistoricoProfissional() {
		return HistoricoProfissional;
	}

	public void setHistoricoProfissional(String historicoProfissional) {
		HistoricoProfissional = historicoProfissional;
	}

	public String getCargoDesejado() {
		return CargoDesejado;
	}

	public void setCargoDesejado(String cargoDesejado) {
		CargoDesejado = cargoDesejado;
	}

}